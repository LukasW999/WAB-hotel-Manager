CREATE TABLE IF NOT EXISTS Kategorie
(
  id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name text,
  preis decimal(10,2),
  betten_anzahl int,
  beschreibung text
);

CREATE TABLE IF NOT EXISTS zimmer
(
  id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nummer int,
  aktiv boolean,
  kategorie_id int,
  FOREIGN KEY (kategorie_id) REFERENCES Kategorie(id)
);

CREATE TABLE IF NOT EXISTS Status
(
  id int generated by default as identity primary key,
  name text
);

CREATE TABLE IF NOT EXISTS Mitarbeiter
(
  id int generated by default as identity primary key,
  vorname text,
  nachname text,
  geburtsdatum date,
  telefonnummer text,
  email text,
  verantwortlichkeit_id int
);

CREATE TABLE IF NOT EXISTS Wird_Betreut_Von
(
  id int generated by default as identity primary key,
  mitarbeiter_id int,
  status_id int,
  foreign key (mitarbeiter_id) references Mitarbeiter(id),
  foreign key (status_id) references Status(id)
);

CREATE TABLE IF NOT EXISTS Gast
(
  id int generated by default as identity primary key,
  vorname text,
  nachname text,
  geburtsdatum date,
  telefonnummer text,
  email text,
  stadt text,
  land text,
  postleitzahl text,
  strasse text,
  hausnummer text
);

CREATE TABLE IF NOT EXISTS mitreisender
(
  vorname text,
  nachname text,
  geburtsdatum date,
  telefonnummer text,
  email text,
  stadt text,
  land text,
  postleitzahl text,
  strasse text,
  hausnummer text,
  zahlender_id int,
  PRIMARY KEY (zahlender_id, vorname, nachname),
  foreign key (zahlender_id) references Gast(id)
);

CREATE TABLE IF NOT EXISTS Gast_Aenderung
(
  id int generated by default as identity primary key,
  alter_wert text,
  neuer_wert text,
  feldname text,
  erstellt TIMESTAMPTZ NOT NULL DEFAULT now(),
  gast_id int,
  foreign key (gast_id) references Gast(id)
);

CREATE TABLE IF NOT EXISTS Reservierung
(
  id int generated by default as identity primary key,
  start date,
  ende date,
  erstellt TIMESTAMPTZ NOT NULL DEFAULT now(),
  gast_id int,
  status_id int,
  zimmer_id int,
  foreign key (gast_id) references Gast(id),
  foreign key (status_id) references Status(id),
  foreign key (zimmer_id) references Zimmer(id)
);

CREATE TABLE IF NOT EXISTS Reservierung_Aenderung
(
  id int generated by default as identity primary key,
  alter_wert text,
  neuer_wert text,
  feldname text,
  erstellt TIMESTAMPTZ NOT NULL DEFAULT now(),
  reservierung_id int,
  foreign key (reservierung_id) references Reservierung(id)
);

